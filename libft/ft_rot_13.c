#include "libft.h"

char	*ft_rot_13(char *str)
{
	int i;

	i = 0;
	if (str)
	{
		while (str[i])
		{
			if (str[i] > 64 && str[i] < 91)
				str[i] = (str[i] - 65 + 13) % 26 + 65;
			if (str[i] > 96 && str[i] < 123)
				str[i] = (str[i] - 97 + 13) % 26 + 97;
			i++;
		}
		return (str);
	}
	return (NULL);
}
